package csv.pojo.Company;


/**
 *
 * @author maher
 */
public class Employee {
    private int    employeeId;
    private String employeeName;
    private String   employeeBirthDate;
    private String employeeMobilePhone;
    private String employeeDepartment;
    private String employeeJopTitle;

    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmployeeBirthDate() {
        return employeeBirthDate;
    }

    public void setEmployeeBirthDate(String employeeBirthDate) {
        this.employeeBirthDate = employeeBirthDate;
    }

    public String getEmployeeMobilePhone() {
        return employeeMobilePhone;
    }

    public void setEmployeeMobilePhone(String employeeMobilePhone) {
        this.employeeMobilePhone = employeeMobilePhone;
    }

    public String getEmployeeDepartment() {
        return employeeDepartment;
    }

    public void setEmployeeDepartment(String employeeDepartment) {
        this.employeeDepartment = employeeDepartment;
    }

    public String getEmployeeJopTitle() {
        return employeeJopTitle;
    }

    public void setEmployeeJopTitle(String employeeJopTitle) {
        this.employeeJopTitle = employeeJopTitle;
    }

    
    
    
    
}
